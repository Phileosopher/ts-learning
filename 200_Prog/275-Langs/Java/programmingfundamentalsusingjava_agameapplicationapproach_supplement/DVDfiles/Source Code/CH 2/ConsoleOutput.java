       public class ConsoleOutput 
       {
         public static void main(String[] args) 
         {	
           // Primitive variable declarations
           int age = 21;
           double weight = 185.25;
           boolean isRaining = false;
           char letter1 = 'A';
    
          System.out.println("The Program's Output Appears Below");
          System.out.println("\t\t\"Hello World!\"");
          System.out.print("\nJohn is " + age + " years old");
          System.out.println(" and weighs "  + weight + " pounds");
          System.out.println("Today it is " + isRaining +
                             " that it is raining\n");
          System.out.println("The first letter of the alphabet is " +
                              letter1);
    
          System.out.println("1/2 + 1/4 = 3/4");//blank lines are ignored
          }
      }
